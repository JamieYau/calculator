function add(num1, num2) {
  return num1 + num2;
}

function subtract(num1, num2) {
    return num1 - num2;
}

function multiply(num1, num2) {
    return num1 * num2;
}

function divide(num1, num2) {
    return num1 / num2;
}

console.log(add(1, 6));
console.log(subtract(1, 6));
console.log(multiply(5, 9));
console.log(divide(5, 9));
